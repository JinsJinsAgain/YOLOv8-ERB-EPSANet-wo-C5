class ERB(nn.Module):
    # CSP Bottleneck with 3 convolutions
    def __init__(self, c1, c2, n=1, g=1, e=0.5):  # ch_in, ch_out, number, shortcut, groups, expansion
        super().__init__()
        c_ = int(c1 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(2 * c_, c2, 1, 1)  # optional act=FReLU(c2)
        self.m = nn.Sequential(*(Bottleneck_eff(c_, c_, g, e=1.0) for _ in range(n)))

    def forward(self, x):
        #print(x.shape)
        conv_1 = self.cv1(x)
        #print(conv_1.shape)
        bottle = self.m(conv_1)
        #print(bottle.shape)
        cat =  torch.cat((conv_1, bottle), 1) 
        #print(cat.shape)
        add = x + cat
        return self.cv2(add)

